public class Week_Records {
    public String week_value;
    public String week_date;
    public String week_no;

    public String getWeekValue() {
        return week_value;
    }

    public void setWeekValue(String value) {
        week_value = value;
    }

    public String getWeekDate() {
        return week_date;
    }

    public void setWeekDate(String date) {
        week_date = date;
    }

    public String getWeekNo() {
        return week_no;
    }

    public void setWeekNo(String weekno) {
        week_no = weekno;
    }

}
